					<h4><span>Frequently</span> Asked Questions</h4>



					<p><strong>MONEY  QUESTIONS</strong></p>



                   <strong>Where does  my money go after I make my donation?</strong>



                    <ul>



                      <li>Your  money, by credit card or by check, is transferred into ETF’s U.S. bank account  and is then transferred via MoneyGram to the church leader who submitted the  need profile that you have suggested. The church leader receives and forwards  the support to the right individual or family.</li>



                    </ul><br>



                    <strong>Do the  people who manage ETF earn a salary?</strong>



                    <ul>



                      <li>The  board members and volunteers of ETF do not receive any compensation for their  time.<!-- As the site grows, some of the volunteers doing data entry and managing  local relationships may in the future receive a reasonable salary. --></li>



                    </ul><br>



                    <strong>What  percentage of my donation goes to the individual or family I suggest?</strong>



                    <ul>



                      <li>100%.  Operational fees are not removed from your gift.  And fees related to transferring funds  internationally are also not removed from your gift. </li>



                    </ul><br>



                    <strong>Why is $50  a month for one year, or $600 in total, the only support option provided?</strong>



                    <ul>



                      <li>The  ETF program is very new, and in the future may expand to include other giving  options. </li>



                      <li>By  choosing one giving level, it is much easier to create and upload need  profiles. Donations at this level may meet a need for six months, or for an  entire year. This is made clear in the need profile.</li>



                      <li>Groups of people are encouraged to register together and select a need together. </li>



                    </ul><br>



                    <strong>How is ETF funded?</strong>



                    <ul>



                      <li>The  operational costs of ETF are covered by a number of generous individual  disciples who believe in the program. These operational costs include the fees  charged by the financial institutions, so 100% of the support given by ETF donors reaches the  intended individuals or families. </li>



                      <li>Donations  from individuals or groups who have registered at ETF are used to meet the  specific needs that are presented on the site. </li>



                    </ul><br>



                    <strong>Is my  donation tax deductible?</strong>



                    <ul>



                      <li>Yes.  ETF is a registered not-for-profit 501c3 organization, which means your  donation is tax deductible in the United States. You will receive a receipt for  your donation after you register.</li>



                    </ul>



                    <strong>&nbsp;</strong>



                    <p><strong>PROGRAM  QUESTIONS</strong></p>



                    <strong>Who  manages ETF?</strong>



                    <ul>



                      <li>A  small group of disciples make up the board of directors. We have chosen not to  put our names on the website; however if you wish to learn more about who we  are you may email us at <a href="mailto:info@especiallythefamily.org">info@especiallythefamily.org</a>.</li>



                    </ul>



                    <br>



                   <strong>What does  ETF do with my personal and financial information? </strong>



                    <ul>



                      <li>The  ETF database only stores your name, physical address and email address. The ETF  web site uses a leading ecommerce provider that uses industry-best practices to  safeguard your financial data. The staff of ETF have no access at any time to  your credit card information. </li>



                    </ul><br>



                    <strong>How does  ETF decide where the money I donate goes?</strong>



                    <ul>



                      <li>After you commit to registering with ETF, you will be given the option to suggest which needs you would like your support to meet. </li>

                      

                      <li>We will do our best to meet the need that you have suggested. In the event that the need you suggested has already been met, or has been removed from the site, the staff members of ETF may ask you to suggest another need. You may also designate upon registration that you would like the staff members of ETF to select a need profile on your behalf.</li>



                    </ul><br>



                   <strong>Does ETF support only members of the ICOC family of churches?</strong>



                    <ul>



                      <li>ETF is committed to supporting people of Christian faith. At the present time we are focusing our efforts on members of the International Churches of Christ throughout the world as they represent the people we are most familiar with. </li>



                    </ul><br>



                    <strong>Who can be a donor to the ETF program? </strong>



                    <ul>



                      <li>Anyone  can join and meet a need at the ETF site. </li>



                    </ul>



                    <p>&nbsp;</p>



                    <p><strong>CONFIDENTIALITY  QUESTIONS</strong></p>



                   <strong>Can I pick  the specific person that receives my support?</strong>



                    <ul>



                      <li>After you have registered and committed to funding a need, you will then be able to suggest the specific need that you would like to meet. We will do our best to meet the need that you have suggested</li>



                    </ul><br>



                    <strong>Will the  person who receives my support know who I am?</strong>



                    <ul>



                      <li>No, your giving will be anonymous. However, you will receive a thank you letter within a month or two and at that time you may choose to initiate communication with the recipient of your donation. You will remain anonymous unless you decide to send additional communications.  </li>



                    </ul><br>



                    <strong>Are the  identities of the people who receive support through ETF kept confidential?</strong>



                    <ul>



                      <li>Yes  and no. </li>



                      <li>Visitors  to the ETF site who have not registered and made a donation cannot see any  confidential information.</li>



                      <li>ETF donors who have completed the registration process may log in and see the need profiles of individuals and families that have an unselected need profile on the site. In these need profiles, first names may be provided. These individuals or families are asked to agree to this when they work with their local church leader to submit their need profile.</li>



                      <li>Once a need has been met, the need profile is removed from the site. ETF members will only learn the full identity of the people whose needs they have met when they receive a thank you letter after one or two months.</li>
